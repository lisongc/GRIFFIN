static Cplx series(Cplx x, Cplx *data, Double error)
// only for well behaved series!
// data: 0. element: point the series is about
//       1. element: number of coefficients
//       2. element: convergence constant = approximation for a_{n+1}/a_n
// error: relative error requested
{
      Cplx dx = x - data[0], r = data[3] + dx * data[4], xn = dx, inc1 = Cplx(0), inc2 = Cplx(0);
      if (dx == 0)
            return (r);
      Double rerror = 2 * abs(r * error * (1 - data[2] * dx) / (data[2] * dx));
      Double maxpoint = abs(data[1]);
      int i;
      for (i = 2; ((i <= maxpoint) && ((i < MIN_EVAL) || (abs(inc1 + inc2) > rerror))); i++)
      {
            xn *= dx;
            inc2 = inc1;
            inc1 = data[i + 3] * xn;
            r += inc1;
      };
      //  if (i>maxpoint-.5)
      //    cerr << "Warning: precision of series calculation may be insufficient!\n";
      return (r);
}

static Cplx multiseries(Cplx x, Cplx **multidata, int datacount, Double error)
// choose the best series out of multidata
{
      int bestseries = 0;
      Cplx dx = x - multidata[bestseries][0];
      Double bestconv = abs(multidata[bestseries][2] * dx / (1 - multidata[bestseries][2] * dx));
      Double conv;
      for (int i = 1; i < datacount; i++)
      {
            dx = x - multidata[i][0];
            conv = abs(multidata[i][2] * dx / (1 - multidata[i][2] * dx));
            if (conv < bestconv)
            {
                  bestconv = conv;
                  bestseries = i;
            };
      };
      return (series(x, multidata[bestseries], error));
}

static Cplx multiseries(Cplx x, Cplx **multidata, int datacount)
{
      return (multiseries(x, multidata, datacount, MINERROR));
}

